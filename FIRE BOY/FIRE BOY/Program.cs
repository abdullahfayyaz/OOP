        static void movePlayerBullet(char[,] maze, bool[] isBulletActive, ref int[] bulletX, ref int[] bulletY, ref int bulletCount)
        {
            for (int index = 0; index < bulletCount; index++)
            {
                if (isBulletActive[index] == true)
                {
                    char next = maze[bulletX[index], bulletY[index] + 1];
                    if (next != ' ')
                    {
                    erasebullet(maze, bulletX[index], bulletY[index]);
                    isBulletActive[bulletCount] = false;
                    makeBulletInactive(index, isBulletActive);
                    makeBulletInactive(index, isBulletActive);
                    }
                    else if (next == ' ')
                    {
                        erasebullet(maze, bulletX[index], bulletY[index]);
                        bulletY[index] = bulletY[index] + 1;
                        printbullet(maze, bulletX[index], bulletY[index]);
                    }
                }
            }
        }
