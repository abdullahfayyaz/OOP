                    maze[player.fireBoyX + row, player.fireBoyY + col] = fireboy[row, col];
                }
            }
        }
        static void eraseFireboy(char[,] maze, char[,] fireboy, Fireboy player)
        {
            for (int row = 0; row < fireboy.GetLength(0); row++)
            {
                for (int col = 0; col < fireboy.GetLength(1); col++)
                {
                    Console.SetCursorPosition(player.fireBoyY + col, player.fireBoyX + row);
                    Console.Write(" ");
                    maze[player.fireBoyX + row, player.fireBoyY + col] = ' ';
                }
            }
        }
        static void fireBoyRight(char[,] maze, char[,] fireboy, Fireboy p)
        {
            if ((maze[p.fireBoyX, p.fireBoyY + 3] == ' ' || maze[p.fireBoyX, p.fireBoyY + 3] == '@') && (maze[p.fireBoyX + 1, p.fireBoyY + 3] == ' ' || maze[p.fireBoyX + 1, p.fireBoyY + 3] == '@') && (maze[p.fireBoyX + 2, p.fireBoyY + 3] == ' ' || maze[p.fireBoyX + 2, p.fireBoyY + 3] == '@'))
            {
                eraseFireboy(maze, fireboy, p);
                p.fireBoyY = p.fireBoyY + 1;
